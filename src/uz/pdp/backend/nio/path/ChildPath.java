/**
 * The ChildPath interface provides constant strings representing
 * the file paths for various child entities in the system.
 */
public interface ChildPath {
    String CHANNELS = "channels.txt";    // File path for channels
    String CHATS = "chats.txt";          // File path for chats
    String GROUPS = "groups.txt";        // File path for groups
    String MESSAGE = "message.txt";      // File path for messages
    String MEMBER = "members.txt";       // File path for members
    String POSTS = "posts.txt";          // File path for posts
    String SUBSCRIBE = "subscribers.txt";// File path for subscribers
    String USERS = "users.txt";          // File path for users
}
